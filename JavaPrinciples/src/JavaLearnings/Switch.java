package JavaLearnings;

public class Switch {

	public static void main(String[] args) {
		int i = 5;
		switch (i){
		case 0:
			System.out.println("Zero");
			break;
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
		default:
			System.out.println("Not a Number");
				
		}
	}

}
